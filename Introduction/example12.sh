#!/usr/bin/bash 

# Operators- arithmetic Operators

# Other arithmetic: 
# 	Addition: 			+
# 	subtraction: 		-
# 	multiplication: 	* 	(Must use \ before *)
# 	division: 			/ 


# Sum of two number- Using backtick and expr:
A=`expr 10 + 10`
echo $A

# division of number-  Using let: 
let A=A+10
echo $A